#include "EndScene.h"
#include "DxLib.h"

int wait_count;

/*********************************************************
	エンド画面　：　初期化処理
	引　数　：　なし
	戻り値　：　なし
**********************************************************/

int EndScene_Initialize(void)
{
	int ret = 0;

	wait_count = 0;

	return ret;
}


/*********************************************************
	ブロック機能　：　更新処理
	引　数　：　なし
	戻り値　：　なし
**********************************************************/

void EndScene_Update(void)
{
	wait_count++;
}

/*********************************************************
	エンド機能　：　修了確認処理
	引　数　：　なし
	戻り値　：　なし
**********************************************************/

int Get_EndTime(void)
{
	if (wait_count > 300)
	{
		return TRUE;
	}
	return FALSE;
}